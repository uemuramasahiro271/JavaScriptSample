
window.onload = function() {
    var p = document.createElement('p');
    p.textContent = "window.onload";
    document.body.appendChild(p);

    setInterval(function() {
        var dd = new Date();
        document.getElementById("clock").innerHTML = dd.toLocaleString();
    }, 1000);

    // マウスオーバー設定
    var mouseover_test1 = document.getElementById("mouseover_test1");
    mouseover_test1.onmouseover = function() {
        mouseover_test1.style.fontSize = "24px";
        mouseover_test1.style.backgroundColor = "#ff7f50";
    }

    mouseover_test1.onmouseleave = function() {
        mouseover_test1.style.fontSize = "18px";
        mouseover_test1.style.backgroundColor = "#ffffff";
    }

    var mouseover_test2 = document.getElementById("mouseover_test2");
    mouseover_test2.onmouseover = function() {
        mouseover_test2.classList.add("mouseover_style");
    }

    mouseover_test2.onmouseleave = function() {
        mouseover_test2.classList.remove("mouseover_style");
    }
}

